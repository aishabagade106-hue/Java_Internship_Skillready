public class addition {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = a + b;
        int d = a - b;
        int mul = a * b;
        int div = a / b;
        System.out.println("Addition: " + c);
        System.out.println("Subtraction: " + d);
        System.out.println("Multiplication: " + mul);
        System.out.println("Division: " + div);
    }
}
